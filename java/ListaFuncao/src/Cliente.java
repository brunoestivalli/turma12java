
public class Cliente {
	
	public String nome;
	public String CPF;
	public int idade;
	public int anoNascimento;
	
	public Cliente( String nome, String CPF, int idade, int anoNascimento) {
		
		
		this.nome = nome;
		this.CPF = CPF;
		this.idade = idade;
		this.anoNascimento = anoNascimento;
	}
	
	
	

}
